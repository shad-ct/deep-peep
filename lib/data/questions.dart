import '../models/question.dart';
import 'questions_ml.dart';

class DataService {
  static List<Question> getQuestionsForCategory(Category category) {
    return allQuestions.where((q) => q.category == category).toList();
  }
  
  // Lazy loaded list
  static List<Question>? _cachedQuestions;

  static List<Question> get allQuestions {
    if (_cachedQuestions != null) return _cachedQuestions!;
    
    List<Question> generated = [];
    int globalId = 1;

    _rawData.forEach((category, questions) {
       final mlQuestions = rawDataMl[category] ?? [];
       int index = 0;
       for (var text in questions) {
         String? textMl;
         if (index < mlQuestions.length) {
           textMl = mlQuestions[index];
         }
         
         generated.add(Question(
           id: 'q_${globalId++}', 
           text: text,
           textMl: textMl,
           category: category
         ));
         index++;
       }
    });

    _cachedQuestions = generated;
    return generated;
  }
}

